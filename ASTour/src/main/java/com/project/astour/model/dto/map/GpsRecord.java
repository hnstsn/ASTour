package com.project.astour.model.dto.map;

public class GpsRecord {
	private String latitude_record;
	private String longitude_record;
	private String time_record;
	private String pk_record;
	private String label_record;
	
	public String getLatitude_record() {
		return latitude_record;
	}
	public void setLatitude_record(String latitude_record) {
		this.latitude_record = latitude_record;
	}
	public String getLongitude_record() {
		return longitude_record;
	}
	public void setLongitude_record(String longitude_record) {
		this.longitude_record = longitude_record;
	}
	public String getTime_record() {
		return time_record;
	}
	public void setTime_record(String time_record) {
		this.time_record = time_record;
	}
	public String getPk_record() {
		return pk_record;
	}
	public void setPk_record(String pk_record) {
		this.pk_record = pk_record;
	}
	public String getLabel_record() {
		return label_record;
	}
	public void setLabel_record(String label_record) {
		this.label_record = label_record;
	}
}
